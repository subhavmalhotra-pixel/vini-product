// ─────────────────────────────────────────────────────────────────────────────
// Funnel bars
// ─────────────────────────────────────────────────────────────────────────────
function FunnelBars({
  steps,
}: {
  steps: Array<{ count: number; label: string; color: string }>;
}) {
  return (
    <div className="flex items-stretch gap-1 sm:gap-2">
      {steps.map((step, i) => (
        <div key={step.label} className="flex-1 flex items-stretch">
          <div className="flex-1 flex flex-col">
            <div
              className={`${step.color} rounded-md flex items-center justify-center text-white font-bold tabular text-xl sm:text-2xl min-h-[56px]`}
            >
              {step.count.toLocaleString()}
            </div>
            <div className="text-center text-[12px] text-text-secondary mt-2">
              {step.label}
            </div>
          </div>
          {i < steps.length - 1 && (
            <div className="text-text-muted self-center px-1 sm:px-1.5 pb-6">→</div>
          )}
        </div>
      ))}
    </div>
  );
}

// ─────────────────────────────────────────────────────────────────────────────
// Trend chart — minimal SVG sparkline
// ─────────────────────────────────────────────────────────────────────────────
function TrendChart() {
  const data = [102, 115, 121, 128, 138, 147];
  const max = Math.max(...data);
  const labels = ["Nov", "Dec", "Jan", "Feb", "Mar", "Apr"];
  return (
    <div>
      <div className="flex items-end gap-2 sm:gap-3 h-32">
        {data.map((v, i) => (
          <div key={i} className="flex-1 flex flex-col items-center justify-end">
            <div
              className="w-full bg-orange-500 rounded-t-sm"
              style={{ height: `${(v / max) * 100}%` }}
            />
            <div className="mt-2 text-[10px] text-text-secondary">
              {labels[i]}
            </div>
            <div className="text-[10px] font-semibold tabular text-text-primary">
              {v}
            </div>
          </div>
        ))}
      </div>
    </div>
  );
}

