/**
 * Full-fidelity email preview matching the reference design.
 *
 * Replaces the small EmailPreviewPane snippet for the right pane of the
 * subscription editor. Renders chrome (orange top bar + dealer header) plus
 * a body that adapts to the selected email type and currently-enabled
 * section toggles.
 */
import type { ReactNode } from "react";

type SectionMap = Record<string, boolean>;

type Props = {
  emailType: "daily" | "weekly" | "monthly" | "eoc";
  enabled: boolean;
  sections: SectionMap;
  dealerName: string;
  dealerAddress?: string;
  periodLabel: string;
};

const TYPE_TITLES: Record<string, string> = {
  daily: "Daily digest",
  weekly: "Weekly performance",
  monthly: "Monthly value report",
  eoc: "End-of-campaign report",
};

const TYPE_PERIOD_PREFIX: Record<string, string> = {
  daily: "Day of",
  weekly: "Week of",
  monthly: "Month of",
  eoc: "Campaign ending",
};

export function RichEmailPreview({
  emailType,
  enabled,
  sections,
  dealerName,
  periodLabel,
}: Props) {
  if (!enabled) {
    return (
      <div className="bg-surface-card border border-border-subtle rounded-xl shadow-sm overflow-hidden">
        <div className="h-1.5 bg-text-muted/30" />
        <div className="px-6 py-16 text-center">
          <h3 className="text-base font-semibold text-text-primary">
            {TYPE_TITLES[emailType]} is paused
          </h3>
          <p className="mt-2 text-sm text-text-secondary max-w-sm mx-auto">
            This recipient won't receive the {TYPE_TITLES[emailType].toLowerCase()}{" "}
            until it's reactivated in the schedule panel.
          </p>
        </div>
      </div>
    );
  }

  return (
    <div className="bg-surface-card border border-border-subtle rounded-xl shadow-sm overflow-hidden">
      {/* Orange accent bar */}
      <div className="h-1.5 bg-orange-500" />

      <div className="px-5 sm:px-7 pt-6 sm:pt-8 pb-6 sm:pb-8 space-y-6">
        {/* Header */}
        <div>
          <h2 className="text-xl sm:text-2xl font-bold leading-tight text-text-primary">
            {dealerName}
          </h2>
          <p className="text-sm text-text-secondary mt-1">
            {TYPE_PERIOD_PREFIX[emailType]} {periodLabel}
          </p>
        </div>

        <div className="border-b border-border-subtle" />

        {/* AI Insight */}
        {sections.ai_narrative !== false && (
          <AiInsightCard emailType={emailType} />
        )}

        {/* Body — different per email type */}
        {emailType === "weekly" && <WeeklyBody sections={sections} />}
        {emailType === "daily" && <DailyBody sections={sections} />}
        {emailType === "monthly" && <MonthlyBody sections={sections} />}
        {emailType === "eoc" && <EOCBody sections={sections} />}
      </div>
    </div>
  );
}

