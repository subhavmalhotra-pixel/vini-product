import { Sparkles, AlertTriangle } from 'lucide-react'
import { format, parseISO } from 'date-fns'
import { InsightCard as InsightCardType } from '../types'

interface InsightCardProps {
  insightCard: InsightCardType
  loading: boolean
}

export default function InsightCard({ insightCard, loading }: InsightCardProps) {
  if (loading) {
    return (
      <div className="bg-white rounded-xl shadow-sm border border-slate-100 p-6 flex flex-col gap-4">
        <div className="flex items-center justify-between">
          <div className="h-5 w-36 bg-slate-200 rounded animate-pulse" />
          <div className="h-5 w-28 bg-slate-200 rounded-full animate-pulse" />
        </div>
        <div className="flex flex-col gap-2">
          <div className="h-4 bg-slate-200 rounded animate-pulse w-full" />
          <div className="h-4 bg-slate-200 rounded animate-pulse w-5/6" />
          <div className="h-4 bg-slate-200 rounded animate-pulse w-4/6" />
        </div>
        <div className="h-3 w-48 bg-slate-100 rounded animate-pulse mt-2" />
      </div>
    )
  }

  const generatedAt = insightCard.generatedAt
    ? format(parseISO(insightCard.generatedAt), "MMM d, yyyy 'at' h:mm a")
    : null

  return (
    <div className="bg-white rounded-xl shadow-sm border border-slate-100 p-6 flex flex-col gap-4">
      <div className="flex items-center justify-between gap-3">
        <span className="text-lg font-semibold text-slate-800">This Week's Insight</span>
        {insightCard.isFallback ? (
          <span className="bg-amber-50 text-amber-700 border border-amber-200 text-xs px-2 py-0.5 rounded-full font-medium flex items-center gap-1 flex-shrink-0">
            <AlertTriangle className="w-3 h-3" />
            AI unavailable · raw data
          </span>
        ) : (
          <span className="bg-orange-50 text-orange-700 border border-orange-200 text-xs px-2 py-0.5 rounded-full font-medium flex items-center gap-1 flex-shrink-0">
            <Sparkles className="w-3 h-3" />
            Generated by AI · Haiku 4.5
          </span>
        )}
      </div>

      {insightCard.isFallback ? (
        <p className="text-sm text-slate-400 leading-relaxed italic">
          AI narrative unavailable. Raw metrics are shown in the summary below.
        </p>
      ) : insightCard.text ? (
        <p className="text-slate-700 leading-relaxed text-sm">{insightCard.text}</p>
      ) : (
        <p className="text-sm text-slate-400 italic">No insight available for this period.</p>
      )}

      {generatedAt && (
        <p className="text-slate-400 text-xs mt-auto pt-2 border-t border-slate-50">
          Generated {generatedAt}
        </p>
      )}
    </div>
  )
}
